<h2 id="Kultur"> Kultur </h2>
<p>Die Heimat der Kaldorei sind die W&auml;lder des gro&szlig;en Kontinents Kalimdor. Alles was die Natur hervorbringt wird von ihnen mit gr&ouml;&szlig;ter Hingabe gesch&uuml;tzt. Die Gesellschaft der Kaldorei fu&szlig;t auf tiefem Vertrauen zueinander. 
</p><br />
<h2 id="Religion"> Religion </h2>
<img class="race right" src="https://images8.alphacoders.com/745/745710.jpg" alt="Priesterin"/> 
<p>Die wichtigste Gottheit der Kaldorei ist die G&ouml;ttin Elune als Symbol des Mondes. Die Existenz von Elune gilt als bewiesen, da sie ihren Anh&auml;ngern, der Schwesternschaft der Elune enorme magische Kr&auml;fte verleiht, die sowohl im Kampf genutzt werden k&ouml;nnen als auch zum Zwecke der Heilung. Die oberste F&uuml;hrerin dieser Schwesternschaft ist die Hohepriesterin, welche als oberste F&uuml;hrerin des Glaubens und des Milit&auml;rs im Mondtempel von Darnassus regiert. Als nachtaktives Volk sind Kaldorei der Sonne eher abgekehrt. 
<br />
Neben der Mondg&ouml;ttin Elune werden auch die Uralten als W&auml;chter Kalimdors verehrt. Besonders die Druiden beten diese an.
</p><br />
<h2 id="Geschlechtertrennung"> Geschlechtertrennung </h2> 
<img class="race left" src="https://images8.alphacoders.com/582/582440.jpg" alt="Kaldorei auf Jagd"/>
<p>Die Rollenverteilung war bei den Kaldorei stets klar strukturiert. Ausnahmen wurden nicht oder nur &auml;u&szlig;erst selten gemacht. Weibliche Kaldorei bestritten die Wege von Priesterinnen, Kriegerinnen oder auch J&auml;gerinnen, w&auml;hrend m&auml;nnliche Kaldorei die Lehren des Cenarius verfolgten um Druiden werden zu k&ouml;nnen. 
<br />
Im Dritten Krieg verzeichneten die Nachtelfen zahlreiche Verluste sodass es zu einer Lockerung der alten Bestimmungen kam. Fortan war es m&ouml;glich, dass auch eine weibliche Kaldorei Druidin werden konnte, so wie es in einigen F&auml;llen auch f&uuml;r einen m&auml;nnlichen Kaldorei m&ouml;glich war zur Schildwache aufzusteigen. Dies sollte daf&uuml;r sorgen, dass die Reihen in den Streitkr&auml;ften der Kaldorei nach und nach wieder gef&uuml;llt werden. 
</p><br />
<h2 id="Regierung"> Regierung </h2> 
<p>Seit &auml;onen werden die Geschicke der Nachtelfen durch die Hohepriesterin Tyrande Whisperwind gelenkt, die faktisch als alleinige Herrscherin ihres Volkes z&auml;hlt. Malfurion Sturmgrimm, Erzdruide und Gemahl von Tyrande nimmt mit dem Zirkel des Cenarius einen beratenden Aspekt in der Regierung der Nachtelfen ein. Da der Zirkel sich aber als neutral sieht h&auml;lt er sich aus den aktiven Entscheidungen meist heraus. 
</p><br />
<h2 id="Technologie"> Technologie </h2> 
<img class="race left" src="https://images6.alphacoders.com/356/356148.png" alt="Kaldorei"/>
<p>Kaldorei sind auf Grund ihres Lebensstils in der lage auf die meisten Technologien zu verzichten, damit z&auml;hlen sie zu den wenigen V&ouml;lkern, die davon nicht abh&auml;ngig sind. Alles was die Kaldorei zum Leben ben&ouml;tigen erlangen sie durch ein Leben im Einklang mit der Natur, den Elementen und den Lebewesen der Wildnis. Die Macht der Druiden gew&auml;hrt ihnen die M&ouml;glichkeit viele G&uuml;ter herzustellen. Ihr Wissen &uuml;ber die verschiedenen Dinge des Lebens erhalten sie durch die weisen Urtume der Lehren. 
Seit sie Mitglied der Allianz sind haben sie auch andere Technologien kennengelernt, deren Nutzen sie durchaus nachvollziehen k&ouml;nnen. Dennoch verlassen sie sich lieber auf die Dinge, die sie kennen und sch&auml;tzen gelernt haben. 
</p><br />
<h2 id="Architektur"> Architektur </h2> 
<p>Die Architektur der Kaldorei hat &uuml;ber die &auml;onen einen starken Wandel erfahren. W&auml;hrend zu Zeiten der Hochgeborenen gro&szlig;e, prunkvolle bauten regierten, deren Ruinen in der heutigen Zeit an vielen Orten Kalimdors gesehen werden k&ouml;nnen, so wird dieser Baustil mittlerweile immer weniger genutzt. 
Naturverbunden wie dieses Volk ist, leben die meisten Elfen heute in Holzh&uuml;tten doer den B&auml;umen selbst. Dabei achten sie darauf, dass nichts besch&auml;digt wird, was der Natur zu eigen ist. 
</p><br />
<h2 id="Kampf und Krieg"> Kampf und Krieg </h2> 
<img class="race right" src="https://images7.alphacoders.com/376/376577.jpg" alt="Kaldorei Armee"/>
<p>In alter Zeit wurde die Armee der Nachtelfen ausschlie&szlig;lich von M&auml;nnern gef&uuml;hrt. Die Priesterinnen der Schwesternschaft galten lediglich als Unterst&uuml;tzung. Magischen Beistand erhielt die Armee durch die Magier der Mondgarde. So schlagkr&auml;ftig diese Armee auch war musste sie jedoch nach der ersten Invasion der brennenden Legion gro&szlig;e Verluste hinnehmen, was zu einer vollst&auml;ndigen Erneuerung f&uuml;hrte. Die Priesterinnen der Schwesternschaft besetzten fortan die f&uuml;hrenden R&auml;nge der Armee. Nach Aufl&ouml;sung der Mondgarde im Zuge der Verbannung der arkanen Magie &uuml;bernahmen die Druiden des Zirkels des Cenarius deren Aufgaben. 
<br />
Als bevorzugte Waffe nutzen die Kaldorei neben dem Bogen vor allem Kriegsgleven, die sowohl als Nah- und Fernkampfwaffe eingesetzt werden k&ouml;nnen als auch als Munition f&uuml;r Belagerungswaffen. 
<br />
F&uuml;r den Luftkampf stehen den Kaldorei Hippogryphen zur Seite, die neben ihren eigenen Kampff&auml;higkeiten auch Bogensch&uuml;tzen als Reiter tragen k&ouml;nnen. 
<br />
Durch ihre zahllosen B&uuml;ndni&szlig;e mit den verschiedenen Naturwesen stehen den Kaldorei im Ernstfall viele Verb&uuml;ndete zur Seite. Dazu z&auml;hlen unter anderem Dryaden, Urtume und Treants. 
</p><br />
<h2 id="Au&szlig;ehen"> Aussehen </h2> 
<img class="race right" src="https://images.alphacoders.com/206/206713.jpg" alt="Kaldorei auf Jagd"/>
<p>Kaldorei sind ein gro&szlig; gewachsenes Volk mit einem muskul&ouml;sen K&ouml;rperbau. Ihre Gr&ouml;&szlig;e kann ohne weiteres bis zu 2,30m betragen. 
Weibliche Kaldorei sind ein Inbegriff der Sch&ouml;nheit ausgestattet mit einem muskul&ouml;sen aber dennoch anmutigen K&ouml;rper, der an eine Katze erinnern mag. Auch sie k&ouml;nnen wie ihre m&auml;nnlichen Artgenossen sehr gro&szlig; werden. 

Als besonders markante Merkmale erkennt man die sehr gro&szlig;en, spitzen Ohren sowie die buschigen Augenbrauen und Fangz&auml;hne im Gebiss, die den Kaldorei ein recht wildes Aussehen verleihen. Haut- und Haarfarben sind vielf&auml;ltig und reichen von einfachem wei&szlig; bis hin zu blau, violett oder schwarz. Die Haare k&ouml;nnen sogar gr&uuml;nliche Farbt&ouml;ne annehmen. 
<br />
Goldene Augen: 
Goldene Augen waren lange Zeit eine gro&szlig;e Seltenheit unter den Kaldorei. Namhafte Figuren der Geschichte, wie z.B. Azshara oder auch Illidan hatten sie. Es hei&szlig;t den Tr&auml;gern goldener Augen st&uuml;nde eine gro&szlig;e Zukunft bevor. 
Nach der Neuordnung, die dem Krieg der Ahnen folgte vermehrte sich das Auftreten goldener Augen. Neben der vermeindlich gro&szlig;en Zukunft wird Kaldorei mit goldenen Augen auch eine besondere Begabung f&uuml;r die druidischen Lehren des Cenarius nachgesagt. 
<br />
Geweih: 
Einigen Kaldorei sind von Geburt an oder im Laufe ihres Lebens Geweihe gewachsen, auch sie stehen f&uuml;r eine besondere Begabung f&uuml;r den Weg des Druiden. 
<br />
Tattoos: 
&uuml;ber die markanten T&auml;towierungen weiblicher Kaldorei existieren keine genauen Informationen, lediglich wilde Spekulationen lassen sich finden. 
</p><br />
<h2 id="Sprachen"> Sprachen </h2> 
<p>Als Hauptsprache wird bei den Kaldorei Darnassisch gesprochen. Mit dem Eintritt in die Allianz erlernten die Elfen zus&auml;tzlich die Allgemeinsprache. 
</p><br />
<h2 id="Verh&auml;ltnis zu anderen V&ouml;lkern"> Verh&auml;ltnis zu anderen V&ouml;lkern </h2>
<img class="race right" src="https://images.alphacoders.com/459/459842.jpg" alt="Tempel Haidene"/>
<p>Das Verh&auml;ltnis der Kaldorei zu anderen V&ouml;lkern l&auml;&szlig;t sich als schwierig bezeichnen, sowohl in der Allianz selbst als auch in der Horde gibt es mitunter sehr gro&szlig;e Spannungen und sogar offene Feindseligkeiten. Grund daf&uuml;r ist die Einsch&auml;tzung der anderen V&ouml;lker als so genannten niedere Rassen. Nur durch stete, diplomatische Bem&uuml;hungen seitens Tyrande und Malfurion sind die Bande zu den V&ouml;lkern der Allianz stabil. Dennoch begegnen die meisten Kaldorei anderen V&ouml;lkern und Kulturen mit Misstrauen. Das einzige Volk dem sie eine gewisse, f&ouml;rmliche Zuneigung gegen&uuml;ber zeigen sind die Gilneer. Schuld daran ist die Mitschuld am Worgenfluch, der die Gilneer befallen hat. 
Direkte Feindseligkeiten existieren besonders gegen&uuml;ber dem Volk der Orcs, da diese die W&auml;lder des Eschentals ausbeuten. Quel´dorei und Sin´dorei werden von den Nachtelfen als Verr&auml;ter betrachtet und auch Trollen gegen&uuml;ber sind sie feindlich gesinnt. 
</p> <br />
